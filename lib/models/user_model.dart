class UserDetails {
  final String name;
  final String email;
  final String phoneNumber;
  final double height;
  final double weight;
  final double age;
  final String gender;
  final String password;

  UserDetails({
    required this.name,
    required this.email,
    required this.phoneNumber,
    required this.height,
    required this.weight,
    required this.age,
    required this.gender,
    required this.password,
  });
}
